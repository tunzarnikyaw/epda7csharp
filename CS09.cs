// Comparison Operators

using System;

namespace MyNamespace
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine( 10 > 3 );  // True
            Console.WriteLine( 10 >= 3 ); // True
            Console.WriteLine( 10 < 3 ); // False
            Console.WriteLine( 10 <= 3 ); // False
            Console.WriteLine( 10 == 3 ); // False
            Console.WriteLine( 10 != 3 ); // True

        }
    }
}